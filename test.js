(function() {
    // Перевіряємо, чи завантажився об'єкт Lampa
    if (typeof Lampa === 'undefined' || !Lampa.Menu) {
        console.error('Плагін Test: Lampa не знайдено!');
        return;
    }

    // Функція, яка буде викликатися при натисканні на кнопку
    const onTestButtonClick = () => {
        // URL цільової сторінки
        const url = 'https://www.antutu.com/html5';

        // Відкриваємо посилання за допомогою вбудованого методу Lampa
        // Другий параметр (порожній рядок) - це категорія, третій (false) - чи використовувати внутрішній плеєр
        Lampa.Link.open(url, '', false);
        
        // Можна також додати невелике повідомлення в консоль для відлагодження
        console.log('Плагін Test: Відкриваю AnTuTu HTML5 Test');
    };

    // Додаємо новий пункт меню
    Lampa.Menu.add({
        alias: 'antutu-test', // Унікальний ідентифікатор пункту меню
        title: 'Test',         // Текст, який буде відображатися на кнопці
        icon: 'globe',         // Назва іконки зі стандартного набору Lampa (можна змінити, наприклад, 'star', 'heart', 'play')
        component: 'custom',   // Тип компоненту
        action: onTestButtonClick // Функція, яка виконається при кліку
    });

    // Невелике повідомлення про успішне завантаження плагіна
    console.log('Плагін Test успішно завантажено. Кнопку "Test" додано до інтерфейсу.');
})();